/* ── Carrega um som do SD para PSRAM ──────────────────────────────── */
static void load_sound(sound_id_t id)
{
    const char *path = k_sd_paths[id];
    if (!sd_file_exists(path)) {
        ESP_LOGW(TAG, "%s não encontrado no SD", path);
        return;
    }

    /* Descobre o tamanho lendo com buf=NULL, maxlen=0 — não funciona com sd_read_file.
     * Estratégia: aloca buffer máximo de 256 KB e lê; trunca depois. */
    const size_t MAX_PCM = 256u * 1024u;  /* 8 s @ 16kHz 16-bit */
    int16_t *buf = heap_caps_malloc(MAX_PCM, MALLOC_CAP_SPIRAM);
    if (!buf) {
        ESP_LOGE(TAG, "PSRAM insuficiente para %s", path);
        return;
    }

    size_t bytes = sd_read_file(path, buf, MAX_PCM);
    if (bytes == 0 || bytes % 2 != 0) {
        ESP_LOGW(TAG, "%s: leitura falhou ou tamanho ímpar (%u bytes)", path, (unsigned)bytes);
        heap_caps_free(buf);
        return;
    }

    /* Redimensiona para o tamanho real (copia + libera excesso) */
    int16_t *exact = heap_caps_realloc(buf, bytes, MALLOC_CAP_SPIRAM);
    g_sounds[id].samples  = exact ? exact : buf;
    g_sounds[id].n_samples = bytes / sizeof(int16_t);

    ESP_LOGI(TAG, "som %d carregado: %u samples (%.1f s)",
             (int)id, (unsigned)g_sounds[id].n_samples,
             (double)g_sounds[id].n_samples / 16000.0);
}

/* ── Init ──────────────────────────────────────────────────────────── */

esp_err_t audio_feedback_init(void)
{
    memset(g_sounds, 0, sizeof(g_sounds));

    /* Fallback BEEP_ACK em flash — sempre disponível */
    g_sounds[SOUND_BEEP_ACK].samples  = (int16_t *)k_beep_ack;
    g_sounds[SOUND_BEEP_ACK].n_samples = BEEP_ACK_SAMPLES;

    /* Carrega sons do SD (substitui o fallback se beep_ack.pcm existir) */
    for (sound_id_t i = 0; i < SOUND_COUNT; i++) {
        load_sound(i);
    }

    audio_playback_task_start();

    ESP_LOGI(TAG, "AudioFeedback init OK");
    return ESP_OK;
}

/* ── API pública ───────────────────────────────────────────────────── */

esp_err_t audio_feedback_play(sound_id_t id)
{
    if (id >= SOUND_COUNT) return ESP_ERR_INVALID_ARG;

    audio_cmd_t cmd = { .id = id };
    BaseType_t ok = xQueueSend(g_audio_cmd_queue, &cmd, 0);
    return (ok == pdTRUE) ? ESP_OK : ESP_ERR_NO_MEM;
}

bool audio_feedback_is_playing(void)
{
    return atomic_load(&g_playing);
}
